Cypress.Commands.add("getAllPosts", () => {
    return cy.request("/api/posts")
})

Cypress.Commands.add("getFirstPost", () =>{
    return cy.request("/api/posts").eq(0)
})

Cypress.Commands.add("getByData", (data) =>{
    return cy.request(`[data-test='${data}']`).eq(0)
})

